function playerIndexForSlot(number: number): number | undefined {
  if (number >= 2 && number <= 12) {
    return number - 2;
  }

  if (number >= 14 && number <= 20) {
    return number - 3;
  }

  return undefined;
}

export function teamPlayerName(teamCode: string, number: number): string | undefined {
  const index = playerIndexForSlot(number);

  if (index === undefined) {
    return undefined;
  }

  const roster = TEAM_PLAYERS[teamCode.toUpperCase()];
  const player = roster?.[index]?.trim();

  if (!player) {
    return undefined;
  }

  return player;
}

export function teamSlotDisplayLabel(
  teamCode: string,
  number: number,
  teamName: string,
): string {
  if (number === 1) {
    return 'Escudo';
  }

  if (number === 13) {
    return 'Foto da seleção';
  }

  return teamPlayerName(teamCode, number) ?? `${teamName} #${number}`;
}
